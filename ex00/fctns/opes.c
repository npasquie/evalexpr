int		ft_mul(int nb1, int nb2)
{
	nb1 = nb1 * nb2;
	return (nb1);
}

int		ft_sub(int nb1, int nb2)
{
	nb1 = nb1 - nb2;
	return (nb1);
}

int		ft_div(int nb1, int nb2)
{
	nb1 = nb1 / nb2;
	return (nb1);
}

int		ft_mod(int nb1, int nb2)
{
	nb1 = nb1 % nb2;
	return (nb1);
}

int		ft_add(int nb1, int nb2)
{
	nb1 = nb1 + nb2;
	return (nb1);
}
