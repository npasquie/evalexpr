int		ft_atoi(char *str)
{
	int		nb;
	int		i;
	int		neg;

	nb = 0;
	neg = 1;
	i = 0;
	while ((*(str + i)) == ' ')
		i++;
	if ((*(str + i)) == '-')
	{
		neg = -1;
		i++;
	}
	else if ((*(str + i)) == '+')
		i++;
	while (*(str + i) >= '0' && *(str + i) <= '9')
	{
		nb = (nb * 10) + (*(str + i) - '0');
		i++;
	}
	return (nb * neg);
}
